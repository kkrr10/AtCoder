    val n = readLine()!!.toLong()
    println(
        when("$n".length) {
            in 1..3 -> { 0 }
            in 4..6 -> { n - 999 }
            in 7..9 -> { (n - 999999) * 2 + 999999 - 999 }
            in 10..12 -> { (n - 999999999) * 3 + (999999999 - 999999) * 2 + 999999 - 999 }
            in 13..15 -> { (n - 999999999999) * 4 + (999999999999 - 999999999) * 3 + (999999999 - 999999) * 2 + 999999 - 999 }
            else -> { 5 + (999999999999999 - 999999999999) * 4 + (999999999999 - 999999999) * 3 + (999999999 - 999999) * 2 + 999999 - 999 }
        }
    )
}
